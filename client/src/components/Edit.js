import * as React from "react";
import PropTypes from "prop-types";
import Button from "@mui/material/Button";
import { styled } from "@mui/material/styles";
import Dialog from "@mui/material/Dialog";
import DialogTitle from "@mui/material/DialogTitle";
import DialogContent from "@mui/material/DialogContent";
import DialogActions from "@mui/material/DialogActions";
import IconButton from "@mui/material/IconButton";
import CloseIcon from "@mui/icons-material/Close";

const BootstrapDialog = styled(Dialog)(({ theme }) => ({
  "& .MuiDialogContent-root": {
    padding: theme.spacing(2),
  },
  "& .MuiDialogActions-root": {
    padding: theme.spacing(1),
  },
}));

const BootstrapDialogTitle = (props) => {
  const { children, onClose, ...other } = props;

  return (
    <DialogTitle sx={{ m: 0, p: 2 }} {...other}>
      {children}
      {onClose ? (
        <IconButton
          aria-label="close"
          onClick={onClose}
          sx={{
            position: "absolute",
            right: 8,
            top: 8,
            color: (theme) => theme.palette.grey[500],
          }}
        >
          <CloseIcon />
        </IconButton>
      ) : null}
    </DialogTitle>
  );
};

BootstrapDialogTitle.propTypes = {
  children: PropTypes.node,
  onClose: PropTypes.func.isRequired,
};

export default function CustomizedDialogs() {
  const [open, setOpen] = React.useState(false);

  const handleClickOpen = () => {
    setOpen(true);
  };
  const handleClose = () => {
    setOpen(false);
  };

  return (
    <div>
      <Button variant="outlined" onClick={handleClickOpen}>
        Open dialog
      </Button>
      <BootstrapDialog
        onClose={handleClose}
        aria-labelledby="customized-dialog-title"
        open={open}
      >
        <BootstrapDialogTitle
          id="customized-dialog-title"
          onClose={handleClose}
        >
          Edit User
        </BootstrapDialogTitle>
        <DialogContent dividers>
          Lorem ipsum dolor sit amet, consectetur adipisicing elit. Eligendi
          dolorum ducimus, deleniti vel modi quod impedit nam nobis ipsum minus?
          Quia cupiditate explicabo vitae rem velit placeat officiis? Adipisci
          at quos vel et, libero necessitatibus sint porro sed nisi, voluptates
          facere? Eveniet, provident velit et debitis illo perferendis quam eius
          optio aut, nam excepturi distinctio recusandae enim blanditiis
          veritatis a laudantium amet quisquam molestias dolorem possimus.
          Quaerat, numquam, cupiditate nesciunt consequatur corporis accusamus
          odio assumenda provident doloribus officia nam. Dolore reiciendis
          repellat optio id deserunt libero recusandae tempore facilis quod, cum
          quas fuga iusto obcaecati alias quasi impedit rem odio?
        </DialogContent>
        <DialogActions>
          <Button autoFocus onClick={handleClose}>
            Save changes
          </Button>
        </DialogActions>
      </BootstrapDialog>
    </div>
  );
}
